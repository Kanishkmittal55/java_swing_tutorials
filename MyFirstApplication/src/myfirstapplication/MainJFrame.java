    private boolean jDivisionDataValidation(double input1, double input2){
        
        boolean dataInvalid = false;
        if (input1 ==0 || input2 == 0 ){
            dataInvalid = true;  
        }
        return dataInvalid;
        
    }
    
    // Code for executing Division between two numbers
    private void jDivideButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDivideButtonActionPerformed
       
        double input1 = Double.valueOf(jInputTextField1.getText());
        double input2 = Double.valueOf(jInputTextField2.getText());
        
        // As one can observe an external function to validate the data provided by the user.
        if (jDivisionDataValidation(input1, input2) ) { 
            jOutputTextField.setText("Division by Zero not possible");
        }
        
        else{
            double output = input1 / input2;
        
            jOutputTextField.setText(String.format("%.4f",output)); 
            // Here we have rounded up the output by using the string.format to four decimal places.
        }
        
        
    }//GEN-LAST:event_jDivideButtonActionPerformed

    // We declared a separate power method for the implementation of different functions as this method will be used
    // everywhere else.
    private double power(double input1, double input2){
        double result = 1; 
        for (int i = 0; i < input2; i++) {
            result *= input1; 
        }
        return result; 
    }
    
