    // This is a separate method to validate the name field on the main login page so that
    // any user is not allowed to login if they leave the name field empty, this method can be used
    // on any other text field validation as well.
    private boolean jLoginValidation(String text){
        text = jNameTextField.getText();
        boolean nameFieldEmpty = false;
        if ((text.equals(""))){
            nameFieldEmpty = true;
          
        }
        return nameFieldEmpty;
    }
    
    private void jLoginButtonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLoginButtonMousePressed
        // TODO add your handling code here:
        
        if (jLoginValidation(jNameTextField.getText())){
          jStatusMessageLabel.setText("Please Enter a name to Login..."); 
          jMainTabbedPane.setSelectedIndex(0);
          jStatusMessageLabel.setForeground(Color.red);
        }
        
        else{
        jMainTabbedPane.setSelectedIndex(1);
        jStatusMessageLabel.setText(jNameTextField.getText()+ " is logged in...");
        jStatusMessageLabel.setForeground(new java.awt.Color(0, 100, 0));
        
        }
    }//GEN-LAST:event_jLoginButtonMousePressed

